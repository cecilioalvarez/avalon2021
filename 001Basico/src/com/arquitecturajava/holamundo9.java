package com.arquitecturajava;

public class holamundo9 {

	public static void main (String[] args) {
		for (int i=0;i<10;i=i+1){
			
			System.out.println(i);
			
		}
		int j=0;
		
		while(j<10) {
			System.out.println(j);
			j++;
		}
		
		int k =0;
		do {
			System.out.println(k);
			k++;
		}while (k<10);
	}
}
