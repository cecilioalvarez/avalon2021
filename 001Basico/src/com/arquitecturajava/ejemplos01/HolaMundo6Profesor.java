package com.arquitecturajava.ejemplos01;

public class HolaMundo6Profesor {

	public static void main(String[] args) {
		
		int numero1 = 2;
		int numero2 = 3;
		int numero4 = 5;
		int numero6 = 5;
		
		System.out.println(numero1 = numero2);
		System.out.println(numero1 == numero2);
		System.out.println(numero4 != numero6);
		System.out.println(numero4 == numero6);
	}

}
