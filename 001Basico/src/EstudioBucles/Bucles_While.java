package EstudioBucles;

public class Bucles_While {
	
	public static void main(String[] args) {
		
		int i=0;
		while (i<=10) {
			System.out.println(i);
			i=i+1;
		}
		
		System.out.println("_______________________");
		
		int n=0;
		while (n<=10) {
			System.out.println("2x"+n+"="+n*2);
			n=n+1;
		}
	}

}
