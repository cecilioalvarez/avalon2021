package Estudio;

public class Acumuladores {

	public static void main(String[] args) {
		
		int nota1 = 5;
		int nota2 = nota1 + 2;
		System.out.println(nota2);
		nota2++;
		System.out.println(nota2);
		nota2++;
		System.out.println(nota2);
		nota2--;
		System.out.println(nota2);
	}

}
