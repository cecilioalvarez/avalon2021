package ejercicio15;

public class Calculadora {
	//Hacemos los metodos staticos pues son metodos comunes en la calculadora
	//Static: compartido por todos
	//Final: no se puede modificar
	public static double suma(int a, int b) {
		return a + b;

	}

	public static double resta(int a, int b) {
		return a - b;

	}
}
