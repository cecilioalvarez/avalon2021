package _003_Bucles;

public class Estudio005_Bucle_For_Inverso {

	public static void main(String[] args) {


		for (int i=10;i>=0;i=i-1) {
			System.out.println(i);
		}
		
		System.out.println("_____________________");
		
		for (int i=10;i>=0;i--) {
			System.out.println(i);
		}

	}

}
