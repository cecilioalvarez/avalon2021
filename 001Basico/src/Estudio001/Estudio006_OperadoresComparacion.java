package Estudio001;

public class Estudio006_OperadoresComparacion {

	public static void main(String[] args) {
		
		int numero1 = 5;
		int numero2 = 7;
		int numero3 = 5;
		
		System.out.println(numero1==numero2);
		System.out.println(numero1!=numero2);
		System.out.println(numero1==numero3);
		System.out.println(numero1 > numero2); 
		System.out.println(numero1 < numero2);

	}

}
